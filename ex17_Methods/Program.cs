


// ---------------------------------Начало программы------------------------------------------------------
Console.Clear();
int X = 0, Y = 0;
InitCoordinatesByUser(ref X, ref Y);        // Вызов метода с передачей параметров по ссылке (ref)
PrintQuarterByCords(X, Y);                  // Вызов метода с передачей параметров по значению
// ----------------------------------Конец программы------------------------------------------------------


// --------------------------------Определение методов---------------------------------------------------------

// --------------------------------------------------------------------------------------------------------------
// Опрелеляем функцию, выполняющую ввод значений координат
static void InitCoordinatesByUser(ref int X, ref int Y)
{
    try
    {
        Console.Write("Введите X: ");
        X = int.Parse(Console.ReadLine() ?? "");

        Console.Write("Введите Y: ");
        Y = int.Parse(Console.ReadLine() ?? "");
    }
    catch (Exception exc)
    {
        Console.WriteLine($"Ошибка ввода данных! {exc.Message}");
        return;
    }
}


// Определяем функцию, принимающую два аргумента (координаты точки x и y)
// и выводящую в консоль номер четверти плоскости, в которой находится эта точка
static void PrintQuarterByCords(int X, int Y)
{
    if (X > 0 && Y > 0)
        Console.WriteLine("1");
    else if (X < 0 && Y > 0)
        Console.WriteLine("2");
    else if (X < 0 && Y < 0)
        Console.WriteLine("3");
    else if (X > 0 && Y < 0)
        Console.WriteLine("4");
    else
        Console.WriteLine("Ошибка! Точка попадает на оси координат!");
}

